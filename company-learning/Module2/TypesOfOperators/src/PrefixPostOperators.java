public class PrefixPostOperators {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Prefix operation" + (++a));
        System.out.println("Value of a is now " + a);
        System.out.println("Postfix operation " + (a++));
        System.out.println("Value of a is now " + a);

    }
}
