package com.deepanshu.VariablesExample;

public class Main {
    public static void main(String[] args) {
        int myVar;
        myVar = 50;
        System.out.println("Value of myVar is " + myVar);
        int anotherVar = 100;
        System.out.println("Value of anotherVar is " + anotherVar);
        myVar = anotherVar;
        System.out.println("Value of myVar is " + myVar);
    }
}
