public class Hello {

    public static void main(String[] args) {
        System.out.println("Hello World");

        int myFirstNumber = (10 + 5) + (10 * 2);
        int mySecondNumber = 12;
        int myThirdNumber = 6;
        int myTotalNumber = myFirstNumber + mySecondNumber + myThirdNumber;
        int myLastOne = 1000 - myTotalNumber;

        System.out.println(myFirstNumber);
        System.out.println(myTotalNumber);
        System.out.println(myLastOne);
    }
}

// Any sequence of characters printed in double quotes is called string literal
